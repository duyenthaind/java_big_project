    public void LoadData() {
        //Connect cn=new Connect();
        cn.getConnect();
        try {
            cn.setMasv(UserManager.instance().getUserName());
            System.out.println("Update info student: " + cn.getMasv());
            rs = cn.GetData("SinhVien");
            jTable1.removeAll();
            String[] tieuDe = {"MaSV", "TenSV", "NgaySinh", "GioiTinh", "DiaChi", "Email",
                "SDT", "MaLop"};
            DefaultTableModel model = new DefaultTableModel(tieuDe, 0);
            while (rs.next()) {
                int GioiTinh = rs.getInt("GioiTinh");
                long NgaySinh = rs.getLong("NgaySinh");
                Vector vt = new Vector();
                vt.add(rs.getString("MaSV"));
                vt.add(rs.getString("TenSV"));
                SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-YYYY");
                vt.add(sdf.format(new Date(NgaySinh * 1000)));
                if (GioiTinh == 0) {
                    vt.add("Nam");
                    Nam.setSelected(true);
                } else {
                    vt.add("Nu");
                    Nu.setSelected(true);
                }
                vt.add(rs.getString("DiaChi"));
                vt.add(rs.getString("Email"));
                vt.add(rs.getString("SDT"));
                vt.add(rs.getString("MaLop"));

                model.addRow(vt);
                txtMaSv.setText(rs.getString("masv"));
                txtMaSv.setEnabled(false);
                txtTenSv.setText(rs.getString("tensv"));
                txtNgaySinh.setText(sdf.format(new Date(NgaySinh * 1000)));
                txtDiaChi.setText(rs.getString("diachi"));
                txtEmail.setText(rs.getString("email"));
                txtSDT.setText(rs.getString("sdt"));

                break;
            }
            jTable1.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(SuaThongTinSinhVienTable.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

