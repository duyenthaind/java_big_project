    private void btnDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangKyActionPerformed
        selectedRow = BangDangKy.getSelectedRow();
        if (selectedRow >= 0) {
            String hocphan = BangDangKy.getValueAt(selectedRow, 0).toString();
            String thoigianhoc = BangDangKy.getValueAt(selectedRow, 2).toString();
            if (check(connect, thoigianhoc, hocphan)) {
                HocPhan hocphanDK = get_hp(hocphan);
                save_hp(hocphanDK);
                reload();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Khong duoc them");
            }

        }


    }//GEN-LAST:event_btnDangKyActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        selectedRow = DaDangKy.getSelectedRow();
        if (selectedRow >= 0) {
            int ret = JOptionPane.showConfirmDialog(null,
                    "Bạn chắc chắn muốn xóa?", "Xóa",
                    JOptionPane.YES_NO_OPTION);
            if (ret == JOptionPane.YES_OPTION) {
                String mahp = DaDangKy.getValueAt(selectedRow, 0).toString();
                delete_hp(mahp);
                reload();
            }

        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnReloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReloadActionPerformed
        reload();
    }//GEN-LAST:event_btnReloadActionPerformed

    public void delete_hp(String mahp) {
        try {
            Config.loadConfig();
            connect = DriverManager.getConnection(Config.DB_URL, Config.DB_USER_NAME, Config.DB_PASSWORD);
            hp.delete(mahp, connect);
        } catch (Exception e) {
            System.out.println("Loi ket noi " + e);
        }

    }

    public HocPhan get_hp(String mahp) {
        HocPhan rs = null;
        try {
            Config.loadConfig();
            connect = DriverManager.getConnection(Config.DB_URL, Config.DB_USER_NAME, Config.DB_PASSWORD);
            rs = (HocPhan) hp.getWithId(mahp, connect);
        } catch (Exception e) {
            System.out.println("Loi ket noi " + e);
        }
        return rs;
    }

    public void save_hp(Object object) {
        try {
            Config.loadConfig();
            connect = DriverManager.getConnection(Config.DB_URL, Config.DB_USER_NAME, Config.DB_PASSWORD);
            hp.save(object, connect);
        } catch (Exception e) {
            System.out.println("Loi ket noi " + e);
        }

    }

    public boolean check(Object object, String thoigianhoc, String mahp) {
        boolean check = false;
        try {
            Config.loadConfig();
            connect = DriverManager.getConnection(Config.DB_URL, Config.DB_USER_NAME, Config.DB_PASSWORD);
            check = hp.check_tkb(connect, thoigianhoc, mahp);
        } catch (Exception e) {
            System.out.println("Loi ket noi " + e);
        }
        return check;
    }

