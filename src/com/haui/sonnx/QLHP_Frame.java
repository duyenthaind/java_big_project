    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String mahp = txtMaHP.getText().trim();
        try {
            Config.loadConfig();
            connect = DriverManager.getConnection(Config.DB_URL, Config.DB_USER_NAME, Config.DB_PASSWORD);
            HocPhan hocphan = (HocPhan) hp.getWithId(mahp, connect);
            CustomTable tb_timKiem = new CustomTable();
            tb_timKiem.timkiem(hocphan);
            BangDangKy.setModel(tb_timKiem);
        } catch (Exception e) {
            System.out.println("Loi ket noi " + e);
        }

    }//GEN-LAST:event_btnTimKiemActionPerformed

